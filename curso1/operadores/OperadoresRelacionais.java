package curso1.operadores;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        double decimal1 = 1.0;
        double decimal2 = 2.0;
        boolean ehMaior = decimal2 > decimal1;
        //System.out.println(decimal2 > decimal1);

        boolean ehMenor = decimal2 < decimal1;
        System.out.println(ehMenor);
    }
}








/* Operadores relacionais
*  O resultado das operaçôes com operadores relacionais é
* sempre um valor booleano verdadeiro ou falso(true or false )
* > : Maior que
* <:  Menor que
* >=: Maior ou igual que
* <=: Menor ou igual que
* ==: igual a
* != Diferente de
 */