package tiposprimitivos;

/* Variáries possuem tipos: primitivos e de referência.
*Primitivos: pré-definidos, simples de criar, inicializar e manter.
* Variável armazena diretamente o seu valor(tamanho fixo)
* Primitivos- 4 categorias: Characteres, Integers, Decimais, Booleans.
* Char: a, C, 2, %.
* Int: 1, 3, 452.
* Dec: 1.3, 2.0
* Booleans: true, false
* Char:  16bits faixa: um caracter ou valores ASCII.
* char nota = 'A' ;
* char simbolo = '%';
*
*Numeros Inteiros
* byte  8bits -128 a 127
* short  16bits -32k a 32k
* int  32 bits -grandes números
* long  64 bits  - números muito grandes
* byte nota = 10;
* int rg = 246866809;
*
*Números Decimais
* float 32bits ~6-7 casas decimais
* double 64bits ~15 casas decimais
* float aliquota = 27.5f;
* double preco = 99.53;
*
*Booleanos
*boolean 1bit true, false
* boolean gostouDoCurso = true;
* boolean escreveuAvaliacao = true;
* boolean javaEhSimples = true;
*
 */